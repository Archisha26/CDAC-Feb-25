class BasicOperations{
	public static void main(String args[])
	{
		int num1 = 125;
		int num2 = 24;
		//Addition
		System.out.println(num1 + "+" + num2 + "=" + (num1+num2));
		//Subtraction
		System.out.println(num1 + "-" + num2 + "=" + (num1-num2));
		//Multiplication
		System.out.println(num1 + "*" + num2 + "=" + (num1*num2));
		//Division
		System.out.println(num1 + "/" + num2 + "=" + (num1/num2));
		//Mod Division
		System.out.println(num1 + "%" + num2 + "=" + (num1%num2));
	}
}